package life.hanabi.utils;

public class OrnamentPosition {
    private final int x;
    private final int y;

    public OrnamentPosition(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
